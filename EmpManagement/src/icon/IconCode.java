
package icon;

public interface IconCode {

    /**
     * Gets the icon name.
     *
     * @return String
     */
    String name();

    /**
     * Gets the icon unicode.
     *
     * @return char
     */
    char getUnicode();

    /**
     * Gets the font family name.
     *
     * @return String
     */
    String getFontFamily();

}
